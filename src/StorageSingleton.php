namespace Sword\Storage;

/**
 * Class Container
 *
 * 单例模式
 */

trait StorageSingleton
{
    private static $instance;

    static function getInstance(...$args)
    {
        if(!isset(self::$instance)){
            self::$instance = new static(...$args);
        }
        return self::$instance;
    }
}